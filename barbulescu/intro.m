INTRO

Actualmente muchos puestos de trabajo dependen de un ordenador, que es la herramienta principal de trabajo de muchos trabajadores y trabajadoras.

¿Pueden las empresa vigilar las comunicaciones de sus trabajadores a través de los ordenadores de la compañía? ¿Incluso cuando son conversaciones privadas, ajenas al trabajo?

El 5 de de septiembre de 2017 la Gran Sala del Tribunal Europeo de Derechos Humanos dictó una Sentencia en la que establece los requisitos para que una empresa en Europa pueda vigilar legalmente las comunicaciones de sus trabajadores a través de los ordenadores de la empresa.

Los criterios de la sentencia se conocen como "Test Barbulescu": a continuación puedes contestar las preguntas para comprobar si la vigilancia de comunicaciones de los empleados de tu empresa es legal.

(EN) INTRO

Many jobs today depend on a computer, which is the main working tool for many workers.

Can companies monitor the communications of their workers through the company's computers? Even when they are private, non-work conversations?

On September 5, 2017, the Grand Chamber of the European Court of Human Rights issued a [Judgment] (http://hudoc.echr.coe.int/spa?i=001-177082) in which it establishes the requirements for a company in Europe to be able to legally monitor the communications of its workers through the company's computers.

The criteria of the ruling are known as the "Barbulescu Test": you can then answer the questions to check whether the communications monitoring of your company's employees is legal.